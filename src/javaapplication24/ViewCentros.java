    public ArrayList<String> ComboCentro() {
        ArrayList<String> arrayList = new ArrayList<>();
        String centros = ""
                + "PREFIX v: <http://centrodesalud.org/ontology#> "
                + "SELECT DISTINCT?sec "
                + "FROM <" + graf + ">"
                + "WHERE { "
                + "?s v:organism ?o ."
                + "?o v:sector ?sec ."
                + "}";
        QueryExecution cboCentros = QueryExecutionFactory.sparqlService(host, centros);
        ResultSet results1 = cboCentros.execSelect();
        while (results1.hasNext()) {
            QuerySolution sol = results1.nextSolution();
            arrayList.add(String.valueOf(sol.getLiteral("sec")));
        }
        cboCentros.close();
        return arrayList;
    }

