    public void Especialidades() {
        ArrayList<String> arrayList = new ArrayList<>();
        String centroS = ""
                + "PREFIX v: <http://centrodesalud.org/ontology#> "
                + "PREFIX rdfs: <http://www.w3.org/2000/01/rdf-schema#> "
                + "SELECT Distinct?n ?s ?nombre "
                + "FROM <" + graf + ">"
                + "WHERE { "
                + "?s v:recordData ?o ."
                + "?s rdfs:name ?nombre ."
                + "?o rdfs:medicalSpecialty ?e ."
                + "?e rdfs:name ?n ."
                + "FILTER(?nombre= '" + ""
                + cboCentro.getSelectedItem().toString()
                + "') ."
                + "}";
        System.out.println("dato: " + cboCentro.getSelectedItem().toString());
        QueryExecution ex1 = QueryExecutionFactory.sparqlService(host, centroS);
        ResultSet results1 = ex1.execSelect();
        DefaultListModel modelList = new DefaultListModel<>();
        while (results1.hasNext()) {
            QuerySolution sol = results1.nextSolution();
            arrayList.add(String.valueOf(sol.getLiteral("n")));
        }
        listEsp.setModel(modelList);
        modelList.addElement(arrayList);
        ex1.close();

    }

