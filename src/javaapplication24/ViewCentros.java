    public ArrayList<String> CentroPorSector() {
        ArrayList<String> arrayList = new ArrayList<>();
        String centro = ""
                + "PREFIX v: <http://centrodesalud.org/ontology#> "
                + "PREFIX rdfs: <http://www.w3.org/2000/01/rdf-schema#> "
                + "SELECT distinct?nombre ?sec "
                + "FROM <" + graf + ">"
                + "WHERE { "
                + "?s a rdfs:Organization ."
                + "?s rdfs:name ?nombre ."
                + "?s v:organism ?o ."
                + "?o v:sector ?sec ."
                + "FILTER(?sec= '" + ""
                + cboSector.getSelectedItem().toString()
                + "') ."
                + "}";
        QueryExecution ex1 = QueryExecutionFactory.sparqlService(host, centro);
        ResultSet results1 = ex1.execSelect();
        while (results1.hasNext()) {
            QuerySolution sol = results1.nextSolution();
            arrayList.add(String.valueOf(sol.getLiteral("nombre")));
        }
        ex1.close();
        return arrayList;

    }

